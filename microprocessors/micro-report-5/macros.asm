;print char
PRINTCH MACRO CHAR
	PUSH AX
	PUSH DX
	MOV DL,CHAR
	MOV AH,2
	INT 21H
	POP DX
	POP AX
ENDM

;print string 
PRINTSTR MACRO STRING
	PUSH AX
	PUSH DX
	MOV DX,OFFSET STRING
	MOV AH,9
	INT 21H
	POP DX
	POP AX
ENDM

;print newline
PRINTLN MACRO
	PUSH AX
	PUSH DX
	MOV	DL,13
	MOV	AH,2
	INT	21H
	MOV	DL,10
	MOV	AH,2
	INT	21H
	POP DX
	POP AX
ENDM

;printtab
PRINTTAB MACRO
	PUSH AX
	PUSH DX
	MOV DL,9
	MOV	AH,2
	INT	21H
	POP DX
	POP AX
ENDM

;read a char and store it in AL
READCH MACRO
	MOV AH,8
	INT 21H
ENDM

;read a char and print it
READNPRINTCH MACRO
	MOV AH,1
	INT 21H
ENDM

;END
EXIT MACRO
	MOV AX,4C00H
	INT 21H
ENDM
